package login;

public class Member {
	
	String pid, pw, pname;

	public Member(String pid, String pw, String pname) {
		super();
		this.pid = pid;
		this.pw = pw;
		this.pname = pname;
	}

	public String getPid() {
		return pid;
	}

	public String getPw() {
		return pw;
	}

	public String getPname() {
		return pname;
	}

	
	
	
}
